var diaSem = 9;

switch(diaSem)
{
    case 1:
        console.log("DOMINGO");
        break;
    case 2:
        console.log("SEGUNDA");
        break;
    case 3:
        console.log("TERÇA");
        break;
    case 4:
        console.log("QUARTA");
        break;
    case 5:
        console.log("QUINTA");
        break;
    case 6:
        console.log("SEXTA");
        break;
    case 7:
        console.log("SABADO");
        break;
    default:
        console.log("Esse dia não existe!!");
};